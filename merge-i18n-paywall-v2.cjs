// ---- merge 로직 ----
const langs = ['ko', 'en', 'vi', 'zh'];

langs.forEach((lang) => {
  const filePath = path.join(LOCALES_DIR, `${lang}.json`);
  
  let existing = {};
  try {
    existing = JSON.parse(fs.readFileSync(filePath, 'utf-8'));
  } catch (e) {
    console.warn(`⚠️  ${lang}.json not found, creating new file`);
  }

  // paywall 네임스페이스 덮어쓰기 (기존 paywall 키 대체)
  existing.paywall = {
    ...(existing.paywall || {}),
    ...data[lang].paywall,
  };

  fs.writeFileSync(filePath, JSON.stringify(existing, null, 2) + '\n', 'utf-8');
  console.log(`✅ ${lang}.json — paywall 키 ${Object.keys(data[lang].paywall).length}개 병합 완료`);
});

